public class whileDemo1 {
    public static void main(String[] args) {
        //利用while循环打印1-100
        /*
        * 初始化语句;
          while(条件判断语句){
              循环体语句;
              条件控制语句;
        * }
        * */
        //开始条件1
        //结束条件100
        int i = 1;
        while (i <= 100){
            System.out.println(i);
            i++;
        }
    }
}
