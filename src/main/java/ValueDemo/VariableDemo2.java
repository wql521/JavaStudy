package ValueDemo;

public class VariableDemo2 {
    public static void main(String[] args) {
        //定义变量记录姓名
        String name = "尘世烟雨";
        //定义变量记录年龄
        int age = 18;
        //定义变量记录性别
        char gender = '男';
        //定义变量记录身高
        double height = 180.1;
        //定义变量记录是否单身
        //true为单身 flase为不单身
        boolean flag = true;

        //打印
        System.out.println(name);
        System.out.println(age);
        System.out.println(gender);
        System.out.println(height);
        System.out.println(flag);

    }
}
