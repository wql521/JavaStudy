package ValueDemo;

public class forDemo {
    public static void main(String[] args) {
        /*for (初始化语句;条件判断语句;条件控制语句){
                循环体语句;
          }*/
        //打印输出1-5
        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }
        System.out.println("---------------");
        //打印输出5-1
        for (int x = 5; x >= 1; x--) {
            System.out.println(x);
        }
    }
}
